package com.example.itmanagement.models;

public class ModelUser {

    String firstName, lastName, email, search, image,cover,uid;

    public ModelUser() {
    }

    public ModelUser(String firstName, String lastName, String email, String search, String image, String cover, String uid) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.search = search;
        this.image = image;
        this.cover = cover;
        this.uid = uid;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSearch() {
        return search;
    }

    public void setSearch(String search) {
        this.search = search;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getCover() {
        return cover;
    }

    public void setCover(String cover) {
        this.cover = cover;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}





